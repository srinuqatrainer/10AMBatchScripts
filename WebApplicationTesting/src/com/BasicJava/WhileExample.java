package com.BasicJava;

public class WhileExample {

	public static void main(String[] args)
	{
		
		int i=1;
		
		while(i<=5)
		{
			System.out.println(i);
			i++;
		}
		
		
		System.out.println("*************************");
		
		int a=5;
		while(a>=3)
		{
			System.out.println(a);
			a--;
		}

	}

}
